import { route } from '@izzyjs/route/client'
import { Alert, Button, Group, Image, MultiSelect, Paper, Stack, TagsInput, Text, TextInput, Textarea } from '@mantine/core'
import { UseFormReturnType } from '@mantine/form'
import { IconPhoto, IconPhotoPlus, IconTrash, IconUpload } from '@tabler/icons-react'
import type { ChangeEvent, RefObject } from 'react'
import TiptapEditor from '~/components/RTE'

type BlogFormValues = {
  id: string
  title: string
  thumbnail_id: string
  description: string
  tags: string[]
  projectIds: string[]
}

type BlogEditorData = {
  id: string
  slug_id: string
}

type ProjectOption = {
  id: string
  title: string
}

type Props = {
  data: BlogEditorData | null
  form: UseFormReturnType<BlogFormValues>
  mutationPending: boolean
  isUploadingThumbnail: boolean
  thumbnailPreviewUrl: string
  thumbnailUploadError: string | null
  thumbnailInputRef: RefObject<HTMLInputElement | null>
  tagSuggestions: string[]
  projects: ProjectOption[]
  content: Record<string, any>
  onContentChange: (value: Record<string, any>) => void
  onOpenMediaLibrary: () => void
  onThumbnailFileChange: (event: ChangeEvent<HTMLInputElement>) => void
  onClearThumbnail: () => void
  formSafeTitle: (value: string) => string
}

export default function BlogEditorTab({
  data,
  form,
  mutationPending,
  isUploadingThumbnail,
  thumbnailPreviewUrl,
  thumbnailUploadError,
  thumbnailInputRef,
  tagSuggestions,
  projects,
  content,
  onContentChange,
  onOpenMediaLibrary,
  onThumbnailFileChange,
  onClearThumbnail,
  formSafeTitle,
}: Props) {
  return (
    <Stack>
      {data && <TextInput label="ID" value={form.values.id} readOnly disabled />}

      <TextInput
        withAsterisk
        label="Title"
        placeholder="Blog title"
        value={form.values.title}
        error={form.errors.title}
        disabled={mutationPending}
        onChange={(event) => form.setFieldValue('title', event.target.value)}
      />

      <TextInput
        label="URL Preview"
        value={
          form.values.title
            ? `${formSafeTitle(form.values.title)}-${data?.slug_id || '{slug_id}'}`
            : '{title}-{slug_id}'
        }
        description="Format: {title}-{slug_id}. slug_id is generated by server."
        readOnly
        disabled
      />

      <Stack gap={8}>
        <Text size="sm" fw={500}>
          Thumbnail
        </Text>

        {thumbnailPreviewUrl ? (
          <Image
            src={thumbnailPreviewUrl}
            alt="Blog thumbnail preview"
            radius="md"
            h={220}
            fit="cover"
            fallbackSrc="https://placehold.co/800x400?text=Thumbnail"
          />
        ) : (
          <Paper withBorder radius="md" p="md">
            <Group>
              <IconPhoto size={18} />
              <Text size="sm" c="dimmed">
                No thumbnail selected
              </Text>
            </Group>
          </Paper>
        )}

        <Group>
          <Button
            variant="outline"
            leftSection={<IconPhotoPlus size={16} />}
            disabled={mutationPending || isUploadingThumbnail}
            onClick={onOpenMediaLibrary}
          >
            Select From Library
          </Button>
          <Button
            variant="outline"
            leftSection={<IconUpload size={16} />}
            loading={isUploadingThumbnail}
            disabled={mutationPending}
            onClick={() => thumbnailInputRef.current?.click()}
          >
            Upload Image
          </Button>
          <Button
            variant="light"
            color="red"
            leftSection={<IconTrash size={16} />}
            disabled={mutationPending || (!form.values.thumbnail_id && !thumbnailPreviewUrl)}
            onClick={onClearThumbnail}
          >
            Remove
          </Button>
        </Group>

        <input
          ref={thumbnailInputRef}
          type="file"
          accept="image/*"
          className="hidden"
          onChange={onThumbnailFileChange}
        />

        <TextInput
          label="Selected Thumbnail ID"
          placeholder="No thumbnail selected"
          value={form.values.thumbnail_id}
          readOnly
          disabled
        />

        {thumbnailUploadError ? (
          <Alert color="red" title="Thumbnail Error">
            {thumbnailUploadError}
          </Alert>
        ) : null}
      </Stack>

      <Textarea
        label="Description"
        placeholder="Short blog description"
        value={form.values.description}
        disabled={mutationPending}
        onChange={(event) => form.setFieldValue('description', event.target.value)}
        autosize
        minRows={3}
        maxRows={6}
      />

      <TagsInput
        label="Tags"
        description="Type and press Enter/comma to add tags. "
        placeholder="Type tags..."
        data={tagSuggestions}
        value={form.values.tags}
        splitChars={[',']}
        clearable
        disabled={mutationPending}
        onChange={(value) => form.setFieldValue('tags', value)}
      />

      <MultiSelect
        label="Projects"
        placeholder="Select related projects"
        data={projects.map((project) => ({
          value: project.id,
          label: project.title,
        }))}
        searchable
        value={form.values.projectIds}
        disabled={mutationPending}
        onChange={(value) => form.setFieldValue('projectIds', value)}
      />

      <div>
        <Text size="sm" fw={500} mb={8}>
          Content
        </Text>
        <TiptapEditor
          content={content}
          onSave={onContentChange}
          getMediaURL={route('api.v1.media.list').path}
          uploadMediaURL={route('api.v1.media.upload').path}
          deleteMediaURL={route('api.v1.media.destroy').path}
          className="min-h-[500px]"
          stickyToolbar={true}
          imageTags={['blog-content']}
        />
      </div>
    </Stack>
  )
}

